void main(){
  String grade = "c";

  switch(grade){
    case 'a' :
    print("best");
    break;

    case 'b':
    print("good");
    break;

    case 'c':
    print("avg");
    break;

    case 'd':
    print("below avg");
    break;

    case 'e':
    print("fail");
    break;

    default:
    print("Invalid grade ");
  }
}